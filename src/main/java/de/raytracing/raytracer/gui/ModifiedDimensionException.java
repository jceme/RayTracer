package de.raytracing.raytracer.gui;

class ModifiedDimensionException extends Exception {

	private static final long serialVersionUID = 5592569624514607177L;

	public ModifiedDimensionException() {
	}

	public ModifiedDimensionException(String message) {
		super(message);
	}

	public ModifiedDimensionException(Throwable cause) {
		super(cause);
	}

	public ModifiedDimensionException(String message, Throwable cause) {
		super(message, cause);
	}

}
